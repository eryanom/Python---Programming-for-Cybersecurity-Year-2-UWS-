# This program defines a simple function that returns a greeting message.

# Define a function named 'greeting' that takes one parameter: name
def greeting(name):
    # The function returns a formatted string saying hello to the given name
    return f"Hello {name}"

# Running of the function
print(greeting("Erya"))   # Call the function with the argument "Erya"
