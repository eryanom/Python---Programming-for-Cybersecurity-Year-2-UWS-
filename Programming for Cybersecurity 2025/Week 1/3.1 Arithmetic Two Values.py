#Request two values from the user
a = int(input("Enter the first value:"))                   #To allow users to input their own numbers instead of hardcoding values.
b = int(input("Enter the second value:"))                  #To allow users to input their own numbers instead of hardcoding values.

print("sum:", a + b)                                       #To find the total when adding two numbers.
print("product:", a * b)                                   #To calculate the result of multiplying the two numbers.
print("Subtraction (first - second):", a - b)              #To find the difference between the first and second number.
print("Division (first / second):", a / b)  #true divison  #To find the exact division result (with decimals).
print("Integer Division:", a // b) #floor division         #To get the whole number part of a division.
print("Modulus:", a % b)                                   #To find the remainder after division.
print("Power:", a ** b)  #a raised to power b              #To find the remainder after division.

